export const getObjectPhotos = (object) => {
  if (!object) return [DEFAULT_IMAGE_URL]

  // Check for photos array first
  if (object.photos && Array.isArray(object.photos) && object.photos.length > 0) {
    const validPhotos = object.photos.filter((photo) => photo && photo.trim() !== "" && photo !== "null")
    if (validPhotos.length > 0) return validPhotos
  }

  // Check for individual photo fields from API
  const photoFields = ["foto", "foto_2", "foto_3", "foto_4", "photo", "gambar", "image"]
  const foundPhotos = []

  for (const field of photoFields) {
    if (object[field] && object[field].trim() !== "" && object[field] !== "null") {
      foundPhotos.push(object[field])
    }
  }

  if (foundPhotos.length > 0) {
    return foundPhotos
  }

  // Return default image
  return [DEFAULT_IMAGE_URL]
}

export const getLokasiName = (object) => {
  if (!object) return "Lokasi tidak tersedia"

  const kotaName = getKotaName(object)
  const kecamatanName = getKecamatanName(object)

  if (kotaName !== "Kota tidak tersedia" && kecamatanName !== "Kecamatan tidak tersedia") {
    return `${kecamatanName}, ${kotaName}`
  } else if (kotaName !== "Kota tidak tersedia") {
    return kotaName
  } else if (kecamatanName !== "Kecamatan tidak tersedia") {
    return kecamatanName
  }

  return "Lokasi tidak tersedia"
}


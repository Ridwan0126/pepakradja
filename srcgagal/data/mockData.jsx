// Map jenis retribusi to category
export const mapJenisRetribusiToCategory = (jenisRetribusi) => {
  if (!jenisRetribusi) return null

  const jenisLower = jenisRetribusi.toLowerCase()

  if (jenisLower.includes("jasa umum") || jenisLower.includes("kesehatan") || jenisLower.includes("kebersihan")) {
    return "retribusi_jasa_umum"
  }

  if (
    jenisLower.includes("jasa usaha") ||
    jenisLower.includes("aset") ||
    jenisLower.includes("pasar") ||
    jenisLower.includes("parkir") ||
    jenisLower.includes("penginapan") ||
    jenisLower.includes("rekreasi") ||
    jenisLower.includes("pariwisata") ||
    jenisLower.includes("pelabuhan")
  ) {
    return "retribusi_jasa_usaha"
  }

  if (jenisLower.includes("perizinan") || jenisLower.includes("tenaga kerja") || jenisLower.includes("pertambangan")) {
    return "retribusi_perizinan_tertentu"
  }

  return null
}

